@app.command("status")
def status_command(
    sidecar: Annotated[
        Path | None,
        typer.Option(
            "--sidecar",
            help=(
                "Path to the stage-events.jsonl sidecar. Defaults to "
                "BFFI_OBSERVABILITY_SIDECAR (typically "
                "<BFFI_DATA_DIR>/stage-events.jsonl)."
            ),
            resolve_path=True,
        ),
    ] = None,
    tail: Annotated[
        bool,
        typer.Option(
            "--tail",
            help="Re-render the summary as new events arrive (polling, 200ms).",
        ),
    ] = False,
    since: Annotated[
        str | None,
        typer.Option(
            "--since",
            help=(
                "Filter to events with ts >= this ISO-8601 timestamp. "
                "Pass 'now' to anchor on the latest start event."
            ),
        ),
    ] = None,
    run_uuid: Annotated[
        str | None,
        typer.Option(
            "--run-uuid",
            help="Filter to a specific run_uuid (default: all runs in the file).",
        ),
    ] = None,
) -> None:
    """Render the current pipeline state from the P-11 stage-events sidecar.

    The events come from Phase A's per-stage emitters; this command is
    a pure consumer — it doesn't write any sidecar entries of its own,
    and the observability bootstrap in ``_root`` parks an emitter that
    stays idle here.
    """
    settings = get_settings()
    if sidecar is None:
        sidecar_str = settings.observability_sidecar.strip()
        if not sidecar_str:
            sidecar = settings.data_dir / "stage-events.jsonl"
        elif sidecar_str.lower() in {"none", "/dev/null", "off"}:
            typer.echo(
                "BFFI_OBSERVABILITY_SIDECAR disables the sidecar — nothing to read.",
                err=True,
            )
            raise typer.Exit(code=2)
        else:
            sidecar = Path(sidecar_str)

    since_dt = None
    if since is not None:
        if since.lower() == "now":
            # Anchor: latest `start` ts in the file. The CLI uses
            # ``now`` as shorthand for "the run that's actually running
            # right now", which in practice is the most recent start.
            rows = status_module.parse_sidecar(sidecar)
            start_rows = [r for r in rows if r.event == "start"]
            if start_rows:
                since_dt = start_rows[-1].ts
        else:
            try:
                from datetime import datetime as _dt

                since_dt = _dt.fromisoformat(since.replace("Z", "+00:00"))
            except ValueError as exc:
                typer.echo(f"Invalid --since timestamp: {since!r} ({exc})", err=True)
                raise typer.Exit(code=2) from exc

    if not tail:
        rows = status_module.parse_sidecar(sidecar, since=since_dt, run_uuid=run_uuid)
        typer.echo(status_module.render(status_module.collate(rows)))
        return

    # --tail: re-render on new events. Clean exit on Ctrl-C.
    try:
        for rendered in status_module.tail(sidecar, since=since_dt, run_uuid=run_uuid):
            typer.echo("\033[2J\033[H" + rendered)  # clear screen, home cursor
    except KeyboardInterrupt:
        typer.echo("", err=True)  # clean newline on exit


@app.command("serve-metrics")
def serve_metrics_command(
    port: Annotated[
        int,
        typer.Option(
            "--port",
            help="HTTP port to expose /metrics on. Default 9100.",
        ),
    ] = 9100,
    sidecar: Annotated[
        list[Path] | None,
        typer.Option(
            "--sidecar",
            help=(
                "Path to a stage-events.jsonl sidecar to tail. "
                "Repeatable — pass --sidecar multiple times to tail "
                "multiple sidecars from one exporter (P-17). Defaults "
                "to BFFI_OBSERVABILITY_SIDECAR / <BFFI_DATA_DIR>/stage-events.jsonl "
                "when neither --sidecar nor --watch-glob is given."
            ),
            resolve_path=True,
        ),
    ] = None,
    watch_glob: Annotated[
        list[str] | None,
        typer.Option(
            "--watch-glob",
            help=(
                "Glob pattern to auto-discover sidecars (P-17). "
                "Repeatable. Rescanned every --glob-rescan-seconds so "
                "fresh bench runs surface without restarting the "
                "exporter. Example: --watch-glob 'scratchpad/**/stage-events.jsonl'."
            ),
        ),
    ] = None,
    glob_rescan_seconds: Annotated[
        float,
        typer.Option(
            "--glob-rescan-seconds",
            help="Interval between --watch-glob rescans. Default 30.0.",
        ),
    ] = 30.0,
    poll_seconds: Annotated[
        float,
        typer.Option(
            "--poll-seconds",
            help="Tail-loop poll interval in seconds. Default 1.0.",
        ),
    ] = 1.0,
) -> None:
    """Run the P-11 Phase D Prometheus exporter.

    Tails one or more stage-events sidecars and exposes the canonical
    pipeline metric vocabulary at ``/metrics`` on the configured port.
    Designed to run for an arbitrarily long time independent of the
    pipeline — survives stage transitions and pipeline restarts.

    P-17: ``--sidecar`` is repeatable, ``--watch-glob`` auto-discovers
    sidecars, and each sidecar's co-located error JSONLs
    (``bibframe/_errors.jsonl``, ``bffi/_validation.jsonl``) are
    derived from the sidecar's PARENT DIR rather than a single global
    ``BFFI_DATA_DIR``. The startup log echoes the resolved set so the
    operator can confirm at a glance which sidecars + error files are
    being watched.

    Pair with the local Prometheus + Grafana stack via
    ``make observability-up`` (configured under ``docker-compose.yml``;
    Grafana auto-loads the provisioned bffi-pipeline dashboard).
    """
    settings = get_settings()
    sidecar_paths: list[Path] = list(sidecar or [])
    watch_globs: list[str] = list(watch_glob or [])

    # Resolve the default sidecar only when neither --sidecar nor
    # --watch-glob was given — operator who passes --watch-glob alone
    # expects discovery, not the implicit default attached too.
    if not sidecar_paths and not watch_globs:
        sidecar_str = settings.observability_sidecar.strip()
        if not sidecar_str:
            sidecar_paths.append(settings.data_dir / "stage-events.jsonl")
        elif sidecar_str.lower() in {"none", "/dev/null", "off"}:
            typer.echo(
                "BFFI_OBSERVABILITY_SIDECAR disables the sidecar — nothing to export.",
                err=True,
            )
            raise typer.Exit(code=2)
        else:
            sidecar_paths.append(Path(sidecar_str))

    # P-17: startup-log echoes the resolved set so the operator sees
    # at a glance which sidecars + co-located error files are being
    # watched — the gotcha from 2026-05-13 (silent stale-sidecar
    # tail) becomes immediately diagnosable.
    typer.echo(
        f"Serving Prometheus metrics at http://0.0.0.0:{port}/metrics",
        err=True,
    )
    if sidecar_paths:
        typer.echo(f"[bffi-pipeline] tailing sidecars ({len(sidecar_paths)}):", err=True)
        for sp in sidecar_paths:
            typer.echo(f"  {sp}", err=True)
            typer.echo(f"    + {sp.parent / 'bibframe' / '_errors.jsonl'}", err=True)
            typer.echo(f"    + {sp.parent / 'bffi' / '_validation.jsonl'}", err=True)
    if watch_globs:
        typer.echo(
            f"[bffi-pipeline] watch-globs ({len(watch_globs)}, rescan every "
            f"{glob_rescan_seconds:g}s):",
            err=True,
        )
        for g in watch_globs:
            typer.echo(f"  {g!r}", err=True)

    # P-32 Phase G: record this exporter's PID + launch argv so
    # `bffi-pipeline runs prune --apply --reset-exporter` can SIGTERM
    # + relaunch us with the same args. The pid file lives at
    # <BFFI_RUNS_ROOT>/.exporter.pid; cleaned up on graceful exit
    # via the exporter's atexit hook.
    import sys as _sys

    from bffi_pipeline.metrics_exporter import serve
    from bffi_pipeline.runs_reset import exporter_pid_file

    pid_path = exporter_pid_file(settings.runs_root)
    argv = [
        _sys.executable,
        "-m",
        "bffi_pipeline",
        "serve-metrics",
        *_reconstruct_serve_argv(
            port=port,
            sidecar_paths=sidecar_paths,
            watch_globs=watch_globs,
            glob_rescan_seconds=glob_rescan_seconds,
            poll_seconds=poll_seconds,
        ),
    ]

    try:
        serve(
            sidecar_paths,
            port=port,
            poll_seconds=poll_seconds,
            watch_globs=watch_globs or None,
            glob_rescan_seconds=glob_rescan_seconds,
            pid_file=pid_path,
            argv=argv,
        )
    except KeyboardInterrupt:
        typer.echo("\nExporter stopped.", err=True)


def _reconstruct_serve_argv(
    *,
    port: int,
    sidecar_paths: list[Path],
    watch_globs: list[str],
    glob_rescan_seconds: float,
    poll_seconds: float,
) -> list[str]:
    """Build the argv that ``--reset-exporter`` would relaunch with.

    Mirrors the operator's original ``bffi-pipeline serve-metrics``
    invocation as faithfully as we can reconstruct from the resolved
    options. Doesn't recover state that was supplied via env (e.g.
    BFFI_PROMETHEUS_URL) — the relaunched process inherits the
    parent's environment via ``subprocess.Popen`` default, so env
    vars survive.
    """
    args: list[str] = ["--port", str(port)]
    for sp in sidecar_paths:
        args += ["--sidecar", str(sp)]
    for g in watch_globs:
        args += ["--watch-glob", g]
    if glob_rescan_seconds != 30.0:  # noqa: PLR2004 — match the CLI default
        args += ["--glob-rescan-seconds", str(glob_rescan_seconds)]
    if poll_seconds != 1.0:
        args += ["--poll-seconds", str(poll_seconds)]
    return args


