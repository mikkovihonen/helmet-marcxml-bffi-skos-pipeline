def _init_observability(settings: Settings) -> StageEventEmitter | None:
    """Construct + register the per-invocation StageEventEmitter (P-11 Phase A).

    Resolves ``settings.observability_sidecar`` and
    ``settings.run_uuid`` into a configured :class:`StageEventEmitter`
    and parks it as the process-wide active emitter so any
    pipeline-stage call can emit without function-signature plumbing.

    ``observability_sidecar`` resolution:
    - empty (default): ``<data_dir>/stage-events.jsonl``.
    - ``"none"`` or ``"/dev/null"`` (case-insensitive): stderr-only,
      no JSONL append.
    - any other value: treated as a path.

    ``run_uuid`` resolution:
    - empty (default): a fresh ``uuid4().hex`` per invocation.
    - any other value: pinned (operator-controlled, useful for
      replay).
    """
    sidecar_str = settings.observability_sidecar.strip()
    sidecar_path: Path | None
    if not sidecar_str:
        sidecar_path = settings.data_dir / "stage-events.jsonl"
    elif sidecar_str.lower() in {"none", "/dev/null", "off"}:
        sidecar_path = None
    else:
        sidecar_path = Path(sidecar_str)

    # P-32 Phase E: ``run_uuid`` is generated by ``Settings._resolve_run_identity``
    # at construction time (so ``data_dir`` resolution can use it). Read
    # it back here for emitter wiring + startup-log echo.
    run_uuid = settings.run_uuid

    # P-32 Phase A: write the initial bffi-run.json manifest into
    # ``data_dir``. Skipped when the operator has explicitly disabled
    # the sidecar (BFFI_OBSERVABILITY_SIDECAR=none) — same opt-out
    # axis. Tests that need a no-manifest emitter construct one
    # directly without going through ``_init_observability``.
    manifest_path: Path | None = None
    if sidecar_path is not None:
        from bffi_pipeline.run_manifest import (
            mark_run_complete as _mark_run_complete,
        )
        from bffi_pipeline.run_manifest import (
            write_initial_manifest,
        )

        manifest_path = write_initial_manifest(
            settings.data_dir,
            run_uuid=run_uuid,
            description=settings.run_description,
            pipeline_git_sha=_resolve_git_sha(),
        )
        # ``atexit`` stamps ``ended_at`` + ``status="completed"`` when
        # Python shuts down normally. A crash / SIGKILL leaves the
        # manifest at ``status="running"``; the operator clears it via
        # ``bffi-pipeline runs mark-complete <uuid> --status=aborted``.
        _atexit.register(_mark_run_complete, settings.data_dir, "completed")

    emitter = StageEventEmitter(
        sidecar_path=sidecar_path,
        run_uuid=run_uuid,
        manifest_path=manifest_path,
    )
    set_active_emitter(emitter)
    # Echo the active run_uuid so operators inspecting a fresh invocation
    # (especially direct ``bffi-pipeline <subcmd>`` calls that bypass the
    # runner scripts) know which run to pick from Grafana's
    # ``$active_run`` dropdown. Stderr so it stays out of any stdout
    # capture / pipe consumers.
    typer.echo(f"[bffi-pipeline] BFFI_RUN_UUID={run_uuid}", err=True)
    # P-32 Phase E: echo the resolved ``data_dir`` with a
    # canonical-vs-override marker so the operator can spot the
    # ``BFFI_DATA_DIR`` escape-hatch case at a glance. "canonical"
    # means the dir was derived from ``runs_root / run_uuid`` (the new
    # default); "override" means the operator set ``BFFI_DATA_DIR``
    # explicitly and the run won't show up in ``bffi-pipeline runs
    # list`` unless they ``adopt`` it.
    if "data_dir" in settings.model_fields_set:
        typer.echo(
            f"[bffi-pipeline] data_dir={settings.data_dir} (override via BFFI_DATA_DIR — "
            f"non-canonical; runs list / prune will not see this run unless you adopt it)",
            err=True,
        )
    else:
        typer.echo(
            f"[bffi-pipeline] data_dir={settings.data_dir} (canonical)",
            err=True,
        )
    return emitter


def _resolve_git_sha() -> str | None:
    """Return the current HEAD SHA, or ``None`` if not in a git checkout.

    Best-effort: shells out to ``git rev-parse HEAD``. Used by P-32
    Phase A to stamp the pipeline-version field on the run manifest.
    """
    import subprocess

    repo_root = Path(__file__).resolve().parent.parent
    try:
        result = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            cwd=repo_root,
            capture_output=True,
            check=True,
            text=True,
            timeout=2.0,
        )
    except (subprocess.SubprocessError, FileNotFoundError, OSError):
        return None
    sha = result.stdout.strip()
    return sha or None


