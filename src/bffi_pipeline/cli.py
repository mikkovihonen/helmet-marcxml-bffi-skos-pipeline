_RUNS_LIST_SORT_KEYS = ("started", "ended", "size", "run_uuid")
_RUNS_LIST_DEFAULT_LIMIT = 50


@runs_app.command("list")
def runs_list_command(
    sort: Annotated[
        str,
        typer.Option(
            "--sort",
            help=(
                "Sort key. One of: started, ended, size, run_uuid. "
                "Default: started (descending — newest first)."
            ),
        ),
    ] = "started",
    status_filter: Annotated[
        str | None,
        typer.Option(
            "--status",
            help=(
                "Comma-separated status filter (e.g. 'completed,running'). "
                "Defaults to no status filter."
            ),
        ),
    ] = None,
    tag: Annotated[
        list[str] | None,
        typer.Option(
            "--tag",
            help="Tag filter; repeatable for AND semantics. Defaults to no tag filter.",
        ),
    ] = None,
    older_than: Annotated[
        str | None,
        typer.Option(
            "--older-than",
            help=(
                "Select runs whose started_at is older than this duration. "
                "Format: NN<unit>; unit in d/w/mo/y (e.g. 30d, 2w, 6mo, 1y)."
            ),
        ),
    ] = None,
    limit: Annotated[
        int,
        typer.Option(
            "--limit",
            help="Maximum rows to render after sort. Default: 50.",
        ),
    ] = _RUNS_LIST_DEFAULT_LIMIT,
    include_legacy: Annotated[
        bool,
        typer.Option(
            "--include-legacy",
            help=(
                "Include directories under BFFI_RUNS_ROOT that lack a "
                "bffi-run.json. They render with synthesised "
                "run_uuid='legacy-<sha1>' and status='unknown'."
            ),
        ),
    ] = False,
    json_output: Annotated[
        bool,
        typer.Option(
            "--json",
            help="Emit JSON array (one object per run) to stdout instead of a table.",
        ),
    ] = False,
    tsv_output: Annotated[
        bool,
        typer.Option(
            "--tsv",
            help="Emit tab-separated rows to stdout instead of a table.",
        ),
    ] = False,
    verbose: Annotated[
        bool,
        typer.Option(
            "--verbose",
            "-v",
            help="Show full started_at ISO timestamp (relative-time hint dropped).",
        ),
    ] = False,
) -> None:
    """List runs under ``BFFI_RUNS_ROOT`` with sort + filter + format options.

    P-32 Phase B. Default rendering is a plain-text table tailored for
    operator terminal width; ``--json`` / ``--tsv`` produce machine-
    consumable output. Legacy dirs (no manifest) are skipped by default
    so the table reflects the canonical run set; pass
    ``--include-legacy`` to see them too.
    """
    from bffi_pipeline.run_manifest import (
        compute_dir_size,
        discover_legacy_dirs,
        discover_runs,
    )

    if sort not in _RUNS_LIST_SORT_KEYS:
        typer.echo(
            f"Invalid --sort={sort!r}; expected one of: {', '.join(_RUNS_LIST_SORT_KEYS)}.",
            err=True,
        )
        raise typer.Exit(code=2)
    if json_output and tsv_output:
        typer.echo("--json and --tsv are mutually exclusive.", err=True)
        raise typer.Exit(code=2)

    older_than_td, statuses, tags = _parse_prune_filters(older_than, status_filter, tag)

    settings = get_settings()
    typer.echo(f"[bffi-pipeline] BFFI_RUNS_ROOT={settings.runs_root}", err=True)

    runs = list(discover_runs(settings.runs_root))
    if include_legacy:
        runs.extend(discover_legacy_dirs(settings.runs_root))

    filtered = _filter_runs_for_listing(
        runs,
        older_than=older_than_td,
        statuses=statuses,
        tags=tags,
    )

    sized: list[tuple[DiscoveredRun, int]] = [(run, compute_dir_size(run.path)) for run in filtered]
    sorted_rows = _sort_runs_for_listing(sized, sort_key=sort)
    truncated_rows = sorted_rows[:limit] if limit > 0 else sorted_rows

    if json_output:
        _render_runs_list_json(truncated_rows)
    elif tsv_output:
        _render_runs_list_tsv(truncated_rows)
    else:
        _render_runs_list_table(
            truncated_rows,
            total_matching=len(sorted_rows),
            verbose=verbose,
            runs_root=settings.runs_root,
        )


@runs_app.command("mark-complete")
def runs_mark_complete_command(
    run_uuid: Annotated[
        str,
        typer.Argument(
            help=(
                "The run_uuid (or a unique prefix) of the run to stamp. "
                "Resolved against BFFI_RUNS_ROOT/<run_uuid>/."
            ),
        ),
    ],
    status: Annotated[
        str,
        typer.Option(
            "--status",
            help=(
                "Status to write. One of: completed, aborted. "
                "Defaults to completed for the manual-completion case."
            ),
        ),
    ] = "completed",
) -> None:
    """Stamp ``ended_at`` + ``status`` on a run's manifest manually.

    Use when the pipeline crashed before the atexit hook fired and
    the manifest is stuck at ``status="running"``. The run dir is
    looked up under ``BFFI_RUNS_ROOT/<run_uuid>``; a unique prefix
    resolves to a single match.
    """
    from typing import cast

    from bffi_pipeline.run_manifest import RunStatus, mark_run_complete

    if status not in ("completed", "aborted"):
        typer.echo(
            f"Invalid --status={status!r}; expected one of: completed, aborted.",
            err=True,
        )
        raise typer.Exit(code=2)

    settings = get_settings()
    runs_root = settings.runs_root
    if not runs_root.is_dir():
        typer.echo(f"BFFI_RUNS_ROOT does not exist: {runs_root}", err=True)
        raise typer.Exit(code=2)

    matches = sorted(d for d in runs_root.iterdir() if d.is_dir() and d.name.startswith(run_uuid))
    if not matches:
        typer.echo(f"No run dir under {runs_root} matches prefix {run_uuid!r}.", err=True)
        raise typer.Exit(code=1)
    if len(matches) > 1:
        names = ", ".join(d.name for d in matches)
        typer.echo(
            f"Ambiguous run_uuid prefix {run_uuid!r}; matches: {names}",
            err=True,
        )
        raise typer.Exit(code=1)

    data_dir = matches[0]
    # ``status`` validated against the literal set above; cast is safe.
    mark_run_complete(data_dir, status=cast("RunStatus", status))
    typer.echo(f"Marked {data_dir.name} as status={status}.")


@runs_app.command("clear-fuseki")
def runs_clear_fuseki_command(
    apply: Annotated[
        bool,
        typer.Option(
            "--apply",
            help="Actually drop the graphs. Default is dry-run (preview only).",
        ),
    ] = False,
    verbose: Annotated[
        bool,
        typer.Option(
            "--verbose",
            "-v",
            help="Show per-graph URI + triple count + namespace classification.",
        ),
    ] = False,
    force_clear: Annotated[
        bool,
        typer.Option(
            "--force-clear",
            help=(
                "Bypass the BFFI_FUSEKI_CLEAR_MAX_TRIPLES safety threshold. "
                "Required when an oversized graph (e.g. a misconfigured "
                "settings.graph_base prefix-matching a vocabulary URI) needs "
                "to be dropped anyway."
            ),
        ),
    ] = False,
) -> None:
    """Manually drop every Fuseki named graph under ``settings.graph_base``.

    P-32 Phase H. Useful as a diagnostic ("what's in Fuseki right
    now?") and as a recovery action ("wipe Fuseki to a known-clean
    state; let the next ``bffi-pipeline load`` rebuild from
    canonical-skosified.ttl"). Vocabulary graphs (YSO, KANTO,
    allars, SLM, MUSO) live in their own URI namespaces outside
    ``graph_base`` and are NOT touched.
    """
    from bffi_pipeline.stages.fuseki_clear import clear_run_output_graphs

    settings = get_settings()
    auth: tuple[str, str] | None = (
        (settings.fuseki_user, settings.fuseki_password)
        if settings.fuseki_user and settings.fuseki_password
        else None
    )
    with httpx.Client(timeout=30.0, auth=auth) as client:
        result = clear_run_output_graphs(
            fuseki_url=settings.fuseki_url,
            graph_base=settings.graph_base,
            max_triples_per_graph=settings.fuseki_clear_max_triples,
            dry_run=not apply,
            force=force_clear,
            client=client,
        )

    if not result.fuseki_reachable:
        typer.echo(
            f"Fuseki unreachable at {settings.fuseki_url}; nothing cleared.",
            err=True,
        )
        raise typer.Exit(code=1)

    verb = "would drop" if not apply else "dropped"
    typer.echo(f"{verb} {len(result.dropped_graphs)} graph(s) under {settings.graph_base}:")
    for graph_uri in result.dropped_graphs:
        if verbose:
            typer.echo(f"  {graph_uri}")
        else:
            typer.echo(f"  {graph_uri.removeprefix(settings.graph_base)}")
    if result.skipped_oversized:
        typer.echo(
            f"\nSkipped {len(result.skipped_oversized)} oversized graph(s) "
            f"(> {settings.fuseki_clear_max_triples:,} triples; "
            f"pass --force-clear to drop):"
        )
        for graph_uri, n in result.skipped_oversized:
            typer.echo(f"  {graph_uri}  ({n:,} triples)")
    typer.echo(f"\nTotal triples surveyed: {result.total_triples_before:,}")
    if not apply:
        typer.echo("\n(--dry-run is the default; pass --apply to drop.)")


@runs_app.command("prune")
def runs_prune_command(
    older_than: Annotated[
        str | None,
        typer.Option(
            "--older-than",
            help=(
                "Select runs whose started_at is older than this duration. "
                "Format: NN<unit>; unit in d/w/mo/y (e.g. 30d, 2w, 6mo, 1y)."
            ),
        ),
    ] = None,
    status_filter: Annotated[
        str | None,
        typer.Option(
            "--status",
            help=(
                "Comma-separated status filter (e.g. 'completed,aborted'). "
                "Defaults to no status filter."
            ),
        ),
    ] = None,
    tag: Annotated[
        list[str] | None,
        typer.Option(
            "--tag",
            help="Tag filter; repeatable for AND semantics. Defaults to no tag filter.",
        ),
    ] = None,
    keep_last: Annotated[
        int | None,
        typer.Option(
            "--keep-last",
            help="Preserve the N most-recent runs (by started_at) regardless of filters.",
        ),
    ] = None,
    keep_tagged: Annotated[
        bool,
        typer.Option(
            "--keep-tagged",
            help="Preserve runs with at least one tag.",
        ),
    ] = False,
    apply: Annotated[
        bool,
        typer.Option(
            "--apply",
            help=(
                "Actually delete the selected dirs. Default is dry-run (preview only). "
                "Requires at least one filter that excludes some runs."
            ),
        ),
    ] = False,
    reset_exporter: Annotated[
        bool,
        typer.Option(
            "--reset-exporter",
            help=(
                "On --apply, also SIGTERM and relaunch the metrics exporter to drop "
                "stale in-memory Prometheus series. Reads PID from "
                "<BFFI_RUNS_ROOT>/.exporter.pid."
            ),
        ),
    ] = False,
    no_relaunch_exporter: Annotated[
        bool,
        typer.Option(
            "--no-relaunch-exporter",
            help=(
                "Pair with --reset-exporter to SIGTERM-without-relaunch; operator "
                "restarts `bffi-pipeline serve-metrics` themselves. Default is "
                "relaunch from the recorded argv."
            ),
        ),
    ] = False,
    reset_prometheus: Annotated[
        bool,
        typer.Option(
            "--reset-prometheus",
            help=(
                "On --apply, also call the Prometheus admin API to delete the pruned "
                "run_uuids' series from TSDB (Phase G — currently logs a warning, no-op)."
            ),
        ),
    ] = False,
    reset_fuseki: Annotated[
        bool,
        typer.Option(
            "--reset-fuseki",
            help=(
                "On --apply, also DROP <graph_base>* named graphs from Fuseki "
                "(Phase H — currently logs a warning, no-op)."
            ),
        ),
    ] = False,
    reset_all: Annotated[
        bool,
        typer.Option(
            "--reset-all",
            help="Shorthand for --reset-exporter + --reset-prometheus + --reset-fuseki.",
        ),
    ] = False,
) -> None:
    """Delete run dirs under ``BFFI_RUNS_ROOT`` matching the filters.

    ``--dry-run`` is the default: the command prints what *would* be
    deleted with sizes and totals, then exits without touching disk.
    Pass ``--apply`` to actually delete; at least one filter that
    excludes some runs (``--older-than``, ``--status``, ``--tag``,
    ``--keep-last``, ``--keep-tagged``) must be set to refuse the
    accidental "delete everything" case.

    Reset flags are accepted today as plumbing for Phases G + H; they
    log warnings and no-op until those phases ship.
    """
    from bffi_pipeline.run_manifest import discover_runs, select_for_pruning

    older_than_td, statuses, tags = _parse_prune_filters(older_than, status_filter, tag)
    has_filter = bool(older_than_td or statuses or tags or keep_last or keep_tagged)
    if apply and not has_filter:
        typer.echo(
            "--apply requires at least one filter that excludes some runs "
            "(--older-than / --status / --tag / --keep-last / --keep-tagged). "
            "Refusing to delete every run; "
            "pass a filter or run without --apply for a dry preview.",
            err=True,
        )
        raise typer.Exit(code=2)

    if reset_all:
        reset_exporter = True
        reset_prometheus = True
        reset_fuseki = True

    settings = get_settings()
    runs = discover_runs(settings.runs_root)
    if not runs:
        typer.echo(f"No runs found under {settings.runs_root}.")
        return

    to_delete, preserved = select_for_pruning(
        runs,
        older_than=older_than_td,
        statuses=statuses,
        tags=tags,
        keep_last=keep_last,
        keep_tagged=keep_tagged,
    )
    if not to_delete:
        typer.echo(f"No runs match the filters. (Discovered {len(runs)} total.)")
        return

    sized, total_bytes = _render_prune_preflight(to_delete, preserved, runs_root=settings.runs_root)

    if not apply:
        typer.echo("\n(--dry-run is the default; pass --apply to delete.)")
        return

    deleted_uuids = _execute_prune(sized, total_bytes)
    _maybe_invoke_resets(
        deleted_uuids,
        reset_exporter=reset_exporter,
        reset_prometheus=reset_prometheus,
        reset_fuseki=reset_fuseki,
        relaunch_exporter=not no_relaunch_exporter,
    )


def _parse_prune_filters(
    older_than: str | None,
    status_filter: str | None,
    tag: list[str] | None,
) -> tuple[timedelta | None, list[str] | None, list[str] | None]:
    """Parse the operator-supplied prune filters into typed values."""
    from bffi_pipeline.run_manifest import parse_duration

    older_than_td = None
    if older_than:
        try:
            older_than_td = parse_duration(older_than)
        except ValueError as exc:
            typer.echo(str(exc), err=True)
            raise typer.Exit(code=2) from exc

    statuses = None
    if status_filter:
        statuses = [s.strip() for s in status_filter.split(",") if s.strip()]

    tags = list(tag) if tag else None
    return older_than_td, statuses, tags


def _render_prune_preflight(
    to_delete: list[DiscoveredRun],
    preserved: list[DiscoveredRun],
    *,
    runs_root: Path,
) -> tuple[list[tuple[DiscoveredRun, int]], int]:
    """Render the dry-run / pre-apply preview; return (sized rows, total bytes)."""
    from bffi_pipeline.run_manifest import compute_dir_size

    typer.echo(f"Pruning {len(to_delete)} run(s) under {runs_root}:")
    sized: list[tuple[DiscoveredRun, int]] = []
    total_bytes = 0
    for run in to_delete:
        size = compute_dir_size(run.path)
        total_bytes += size
        sized.append((run, size))
        typer.echo(
            f"  {run.manifest.run_uuid[:12]}... "
            f"started={run.manifest.started_at.isoformat()} "
            f"status={run.manifest.status} "
            f"size={_human_bytes(size)}  "
            f"({run.manifest.description or '—'})"
        )
    typer.echo(f"Total to free: {_human_bytes(total_bytes)}")

    if preserved:
        typer.echo(f"\nPreserved by --keep-tagged / --keep-last ({len(preserved)}):")
        for run in preserved:
            typer.echo(
                f"  {run.manifest.run_uuid[:12]}... "
                f"tags={','.join(run.manifest.tags) or '—'}  "
                f"({run.manifest.description or '—'})"
            )
    return sized, total_bytes


def _execute_prune(
    sized: list[tuple[DiscoveredRun, int]],
    total_bytes: int,
) -> list[str]:
    """Hard-delete the selected run dirs; return the deleted run_uuids."""
    import shutil

    typer.echo("\nDeleting...")
    deleted_uuids: list[str] = []
    for run, _size in sized:
        shutil.rmtree(run.path)
        deleted_uuids.append(run.manifest.run_uuid)
        typer.echo(f"  removed {run.path}")
    typer.echo(f"Pruned {len(deleted_uuids)} run(s); freed {_human_bytes(total_bytes)}.")
    return deleted_uuids


def _maybe_invoke_resets(
    deleted_uuids: list[str],
    *,
    reset_exporter: bool,
    reset_prometheus: bool,
    reset_fuseki: bool,
    relaunch_exporter: bool = True,
) -> None:
    """Invoke the Phase G / H reset helpers based on the flag set."""
    from bffi_pipeline.runs_reset import (
        reset_exporter as _reset_exporter,
    )
    from bffi_pipeline.runs_reset import (
        reset_fuseki as _reset_fuseki,
    )
    from bffi_pipeline.runs_reset import (
        reset_prometheus as _reset_prometheus,
    )

    if reset_exporter:
        _reset_exporter(relaunch=relaunch_exporter)
    if reset_prometheus:
        _reset_prometheus(deleted_uuids)
    if reset_fuseki:
        _reset_fuseki()


def _filter_runs_for_listing(
    runs: list[DiscoveredRun],
    *,
    older_than: timedelta | None,
    statuses: list[str] | None,
    tags: list[str] | None,
) -> list[DiscoveredRun]:
    """Apply ``older-than`` / ``status`` / ``tag`` filters in AND semantics.

    Mirrors the ``select_for_pruning`` predicate but without the
    preservation rules (``keep_last`` / ``keep_tagged``) — listing just
    wants the included set.
    """
    if older_than is None and not statuses and not tags:
        return list(runs)
    current = datetime.now(UTC)
    out: list[DiscoveredRun] = []
    for run in runs:
        if older_than is not None:
            started = run.manifest.started_at
            if started.tzinfo is None:
                started = started.replace(tzinfo=UTC)
            if current - started < older_than:
                continue
        if statuses and run.manifest.status not in statuses:
            continue
        if tags:
            run_tags = set(run.manifest.tags)
            if not all(t in run_tags for t in tags):
                continue
        out.append(run)
    return out


def _sort_runs_for_listing(
    sized: list[tuple[DiscoveredRun, int]],
    *,
    sort_key: str,
) -> list[tuple[DiscoveredRun, int]]:
    """Sort ``(run, size)`` tuples for ``runs list``.

    ``started`` and ``ended`` are descending (newest first — operator
    expects "what's most recent"). ``size`` is descending (biggest
    first — operator expects "what's eating disk"). ``run_uuid`` is
    ascending (alphabetical — stable for diffing JSON output).
    """
    if sort_key == "started":
        return sorted(sized, key=lambda row: row[0].manifest.started_at, reverse=True)
    if sort_key == "ended":
        return sorted(
            sized,
            key=lambda row: row[0].manifest.ended_at or row[0].manifest.started_at,
            reverse=True,
        )
    if sort_key == "size":
        return sorted(sized, key=lambda row: row[1], reverse=True)
    # "run_uuid"
    return sorted(sized, key=lambda row: row[0].manifest.run_uuid)


def _render_runs_list_table(
    rows: list[tuple[DiscoveredRun, int]],
    *,
    total_matching: int,
    verbose: bool,
    runs_root: Path,
) -> None:
    """Render a fixed-column table to stdout."""
    if not rows:
        typer.echo(f"No runs match the filters under {runs_root}.")
        return

    header = f"{'RUN_UUID':<14}{'STARTED':<22}{'STATUS':<12}{'SIZE':>10}  TAGS / DESCRIPTION"
    typer.echo(header)
    typer.echo("-" * len(header))
    for run, size in rows:
        run_id = run.manifest.run_uuid[:12]
        started = (
            run.manifest.started_at.isoformat()
            if verbose
            else _relative_time(run.manifest.started_at)
        )
        tags = ",".join(run.manifest.tags) if run.manifest.tags else "—"
        desc = run.manifest.description or ""
        trailing = f"{tags}  {desc}".rstrip()
        typer.echo(
            f"{run_id:<14}"
            f"{started:<22}"
            f"{run.manifest.status:<12}"
            f"{_human_bytes(size):>10}  "
            f"{trailing}"
        )
    if len(rows) < total_matching:
        typer.echo(f"\n(Showing {len(rows)} of {total_matching} matching; pass --limit to widen.)")


def _render_runs_list_json(rows: list[tuple[DiscoveredRun, int]]) -> None:
    """Render ``rows`` as a JSON array of run objects to stdout."""
    import json as _json

    payload = [_run_to_listing_dict(run, size) for run, size in rows]
    typer.echo(_json.dumps(payload, indent=2, ensure_ascii=False))


def _render_runs_list_tsv(rows: list[tuple[DiscoveredRun, int]]) -> None:
    """Render ``rows`` as TSV (header + one row per run) to stdout."""
    columns = (
        "run_uuid",
        "started_at",
        "ended_at",
        "status",
        "size_bytes",
        "tags",
        "description",
    )
    typer.echo("\t".join(columns))
    for run, size in rows:
        ended = run.manifest.ended_at.isoformat() if run.manifest.ended_at else ""
        tags = ",".join(run.manifest.tags)
        desc = (run.manifest.description or "").replace("\t", " ").replace("\n", " ")
        typer.echo(
            "\t".join(
                (
                    run.manifest.run_uuid,
                    run.manifest.started_at.isoformat(),
                    ended,
                    run.manifest.status,
                    str(size),
                    tags,
                    desc,
                )
            )
        )


def _run_to_listing_dict(run: DiscoveredRun, size: int) -> dict[str, object]:
    """Project a :class:`DiscoveredRun` + size into the JSON listing schema."""
    return {
        "run_uuid": run.manifest.run_uuid,
        "started_at": run.manifest.started_at.isoformat(),
        "ended_at": (run.manifest.ended_at.isoformat() if run.manifest.ended_at else None),
        "status": run.manifest.status,
        "size_bytes": size,
        "tags": list(run.manifest.tags),
        "description": run.manifest.description,
        "path": str(run.path),
    }


_RELATIVE_TIME_WEEK_SECONDS = 7 * 24 * 60 * 60
_RELATIVE_TIME_DAY_SECONDS = 24 * 60 * 60
_RELATIVE_TIME_HOUR_SECONDS = 60 * 60
_RELATIVE_TIME_MINUTE_SECONDS = 60


def _relative_time(when: datetime) -> str:
    """Render a datetime as a short relative-time hint (e.g. ``3d ago``)."""
    now = datetime.now(UTC)
    anchor = when if when.tzinfo is not None else when.replace(tzinfo=UTC)
    delta = now - anchor
    secs = max(int(delta.total_seconds()), 0)
    if secs >= _RELATIVE_TIME_WEEK_SECONDS:
        return f"{secs // _RELATIVE_TIME_WEEK_SECONDS}w ago"
    if secs >= _RELATIVE_TIME_DAY_SECONDS:
        return f"{secs // _RELATIVE_TIME_DAY_SECONDS}d ago"
    if secs >= _RELATIVE_TIME_HOUR_SECONDS:
        return f"{secs // _RELATIVE_TIME_HOUR_SECONDS}h ago"
    if secs >= _RELATIVE_TIME_MINUTE_SECONDS:
        return f"{secs // _RELATIVE_TIME_MINUTE_SECONDS}m ago"
    return "just now"


_BYTES_PER_KB = 1024
_BYTES_PER_MB = 1024 * 1024
_BYTES_PER_GB = 1024 * 1024 * 1024


def _human_bytes(n: int) -> str:
    """Render a byte count as KB / MB / GB for operator-facing output."""
    if n < _BYTES_PER_KB:
        return f"{n} B"
    if n < _BYTES_PER_MB:
        return f"{n / _BYTES_PER_KB:.1f} KB"
    if n < _BYTES_PER_GB:
        return f"{n / _BYTES_PER_MB:.1f} MB"
    return f"{n / _BYTES_PER_GB:.2f} GB"


